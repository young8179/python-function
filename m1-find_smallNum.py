# Find smallest number

def smallest(numbers):
    return min(numbers)

number_list = [3, 13, 24, 542, 1, 45, 36, -109]
check_smallest = smallest(number_list)
print(check_smallest)


def largest(numbers):
    return max(numbers)

number_list = [3, 13, 24, 542, 1, 45, 36, -109]
check_largest = largest(number_list)
print(check_largest)